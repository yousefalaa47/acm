
package twonum;
import java .util.* ;
public class Twonum {
    public static void main(String[] args) {
        Scanner input = new Scanner(System.in);
        long x = input.nextInt();
        long y = input.nextInt();
        System.out.println(x+ " + "+ y +" = " + (x + y));
        System.out.println(x+ " * "+ y +" = " + (x * y));
        System.out.println(x+ " - "+ y +" = " + (x - y));
        
    }
    
}
